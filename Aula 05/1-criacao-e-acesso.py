# Lista de notas: estrutura linear dinâmica em Python.
# Conceito teórico: lista como array redimensionável, com armazenamento sequencial.
notas = [8.5, 7.0, 9.2, 6.8]

# Acesso por índice (complexidade O(1)).
# Demonstra o conceito de acesso direto típico de vetores.
print("Primeira nota:", notas[0])

# Índice negativo acessa posições relativas ao final da lista.
# Reforça a ideia de ordenação e posição relativa na estrutura linear.
print("Última nota:", notas[-1])