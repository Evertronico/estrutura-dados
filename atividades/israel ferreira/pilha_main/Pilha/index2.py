# cria uma pilha usando uma lista
pilha = []

# simula um push -> adiciona ao topo
pilha.append("A")
pilha.append("B")
pilha.append("C")

print("Pilha atual:", pilha)

# simula um pop -> remove do topo
removido = pilha.pop()

print("Elemento removido:", removido)
print("Pilha atual:", pilha)                            