# Estrutura de dados linear contendo múltiplos valores
numeros = [3, 7, 2, 10, 5]

# Laço de repetição para percorrer os dados armazenados
for n in numeros:
    # Estrutura condicional aplicada a cada elemento da estrutura
    # A condição avalia uma propriedade do dado
    if n % 2 == 0:
        # Execução quando a condição é verdadeira
        print(n, "é par")  # Informação gerada pela análise do dado
    else:
        # Execução alternativa quando a condição é falsa
        print(n, "é ímpar")  # Informação resultante do processamento
